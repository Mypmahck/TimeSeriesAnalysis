package cn.InstFS.wkr.NetworkMining.Miner.NetworkMiner;

public class Pair {
	public int begin;
	public int end;

	public Pair(int begin, int end) {
		this.begin = begin;
		this.end = end;
	}

	public int getBegin() {
		return begin;
	}

	public void setBegin(int begin) {
		this.begin = begin;
	}

	public int getEnd() {
		return end;
	}

	public void setEnd(int end) {
		this.end = end;
	}
}
